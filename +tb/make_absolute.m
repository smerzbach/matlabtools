% Make an input path to a file or folder absolute.
function absolute_path = make_absolute(input)
    java_file = java.io.File(input);
    if ~java_file.isAbsolute()
        java_file = java.io.File(fullfile(pwd, input));
    end
    
    absolute_path = char(java_file.getCanonicalPath());
end
